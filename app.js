const root = document.documentElement;
const toggle = document.querySelector("[data-lang-toggle]");
const storedLanguage = window.localStorage.getItem("siteLanguage");
let activeLanguage = storedLanguage === "fa" ? "fa" : "ru";

function setLanguage(language) {
  activeLanguage = language;
  root.lang = language;
  document.body.classList.toggle("lang-fa", language === "fa");
  document.body.classList.toggle("lang-ru", language === "ru");
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (translations[language][key]) {
      node.textContent = translations[language][key];
    }
  });
  window.localStorage.setItem("siteLanguage", language);
}

toggle.addEventListener("click", () => {
  setLanguage(activeLanguage === "ru" ? "fa" : "ru");
});

const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
      }
    });
  },
  { threshold: 0.12 }
);

document.querySelectorAll(".reveal").forEach((node) => observer.observe(node));

setLanguage(activeLanguage);
